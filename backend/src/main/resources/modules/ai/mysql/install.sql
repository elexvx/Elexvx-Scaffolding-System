CREATE TABLE IF NOT EXISTS ai_provider_settings (
  id BIGINT NOT NULL AUTO_INCREMENT,
  name VARCHAR(64) NOT NULL,
  vendor VARCHAR(32) NOT NULL,
  base_url VARCHAR(255) NOT NULL,
  endpoint_path VARCHAR(255) NULL DEFAULT NULL,
  model VARCHAR(128) NULL DEFAULT NULL,
  api_key VARCHAR(512) NULL DEFAULT NULL,
  api_version VARCHAR(64) NULL DEFAULT NULL,
  temperature DOUBLE NULL DEFAULT NULL,
  max_tokens INT NULL DEFAULT NULL,
  is_default TINYINT NULL DEFAULT NULL,
  enabled TINYINT NULL DEFAULT NULL,
  extra_headers VARCHAR(2000) NULL DEFAULT NULL,
  remark VARCHAR(512) NULL DEFAULT NULL,
  last_test_status VARCHAR(32) NULL DEFAULT NULL,
  last_test_message VARCHAR(512) NULL DEFAULT NULL,
  last_tested_at DATETIME NULL DEFAULT NULL,
  created_at DATETIME NULL DEFAULT NULL,
  updated_at DATETIME NULL DEFAULT NULL,
  PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
