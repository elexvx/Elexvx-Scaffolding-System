CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  account VARCHAR(64) NOT NULL UNIQUE,
  guid VARCHAR(36) NOT NULL UNIQUE,
  name VARCHAR(64) NOT NULL,
  password_hash VARCHAR(100) NOT NULL,
  mobile VARCHAR(20),
  phone VARCHAR(20),
  email VARCHAR(100),
  id_card VARCHAR(32),
  seat VARCHAR(50),
  entity VARCHAR(100),
  leader VARCHAR(64),
  position VARCHAR(64),
  join_day DATE,
  team VARCHAR(255),
  gender VARCHAR(10),
  nickname VARCHAR(64),
  province_id INTEGER,
  province VARCHAR(100),
  city_id INTEGER,
  city VARCHAR(100),
  district_id INTEGER,
  district VARCHAR(100),
  town_id INTEGER,
  town VARCHAR(100),
  street_id INTEGER,
  street VARCHAR(100),
  zip_code VARCHAR(12),
  address VARCHAR(255),
  introduction TEXT,
  avatar VARCHAR(255),
  tags VARCHAR(255),
  status SMALLINT NOT NULL DEFAULT 1,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
